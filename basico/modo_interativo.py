# Ao digitar "python3" no terminal, o interpretador Python é ativado em modo interativo.
# O prompt ">>>" indica que o interpretador está pronto para receber comandos.
# Para sair do modo interativo, digite "exit()" ou "Ctrl+D".

# O comando dir() exibe uma lista de todos os nomes definidos no escopo atual.
# Nessa lista, estão incluídos os nomes de variáveis, módulos, funções, etc.

# O comando help() exibe uma mensagem de ajuda interativa.
# Essas mensagens de ajuda são úteis para entender como usar um objeto ou função.
