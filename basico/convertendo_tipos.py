print(int(10.5)) # converte float para int, truncando a parte decimal
print(float(10)) # converte int para float
print(str(10)) # converte int para str

print(bool(0)) # converte int para bool, 0 é considerado False
print(bool(1)) # converte int para bool, 1 é considerado True

print(bool("")) # converte str para bool, string vazia é considerada False
print(bool(" ")) # converte str para bool, string com espaço é considerada True

valor = 10.5
valor_str = str(valor) # converte float para str

print(type(valor)) # imprime o tipo da variável valor
print(type(valor_str)) # imprime o tipo da variável valor_str


print(10 / 5) # divisão normal, resultado é float
print(10 // 5) # divisão inteira, resultado é int
print(10 % 3) # resto da divisão, resultado é int

