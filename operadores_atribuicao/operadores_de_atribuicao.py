saldo = 100

# Adiciona 10 ao saldo
saldo += 10
print(saldo)  # Saída: 110

# Subtrai 5 do saldo
saldo -= 5
print(saldo)  # Saída: 105

# Multiplica o saldo por 2
saldo *= 2
print(saldo)  # Saída: 210

# Divide o saldo por 3
saldo /= 3
print(saldo)  # Saída: 70.0

# Módulo do saldo por 6
saldo %= 6
print(saldo)  # Saída: 4.0

# Potência do saldo elevado a 2
saldo **= 2
print(saldo)  # Saída: 16.0

# Divisão inteira do saldo por 3
saldo //= 3
print(saldo)  # Saída: 5.0
